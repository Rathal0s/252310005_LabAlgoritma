#include <iostream>
using namespace std;

int main() {
    double A, B;

    cout << "Jumlah A: ";
    cin >> A;
    cout << "Jumlah B: ";
    cin >> B;


    cout<< "No 1. Pertambahan"<<endl;
    cout << "A = " << A <<endl;
    cout << "B = " << B <<endl;

    cout << "A + B = " << A + B <<endl;
    cout << "Hasil A + B = " << A + B <<endl<<endl<<endl<<endl;

    cout<< "No 2. Pengurangan"<<endl;
     cout << "A = " << A <<endl;
    cout << "B = " << B <<endl;

    
    cout << "A - B = " << A - B <<endl;
    cout << "Hasil A - B = " << A - B <<endl<<endl<<endl<<endl;

    cout<< "No 3. Perkalian"<<endl;
     cout << "A = " << A <<endl;
    cout << "B = " << B <<endl;

    cout << "A * B = " << A * B <<endl;
    cout << "Hasil A * B = " << A * B <<endl<<endl<<endl<<endl;

    cout<< "No 4. Pembagian"<<endl;
    cout << "A = " << A <<endl;
    cout << "B = " << B <<endl;

    cout << "A / B = " << A / B <<endl;
    cout << "Hasil A / B = " << A / B <<endl<<endl<<endl<<endl;

}
